package Clases;

public class Dupla {
	private int probabilidad;
	private int simbolo;
	public Dupla(int probabilidad, int simbolo) {
		super();
		this.probabilidad = probabilidad;
		this.simbolo = simbolo;
	}
	public int getProbabilidad() {
		return probabilidad;
	}
	public int getSimbolo() {
		return simbolo;
	}


}
